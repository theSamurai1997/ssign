"""Tests for validate_macsyfinder_systems.py.

Three layers of behaviour:

1. parse_sys_id() — model_fqn → sys_id regex → identity fallback chain.
2. parse_macsyfinder_results() — TSV reader that tolerates header comments,
   blank lines, sample-prefixed filenames, and missing files.
3. main() — wholeness threshold, excluded-systems filter, two-file output
   (system summary + component detail).
"""

