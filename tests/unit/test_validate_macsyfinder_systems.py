# ---------------------------------------------------------------------------
# parse_sys_id — model_fqn / double-underscore / regex / identity fallback
# ---------------------------------------------------------------------------


@pytest.mark.parametrize(
    "sys_id, model_fqn, expected",
    [
        # 1. model_fqn wins when provided
        ("anything", "TXSScan/bacteria/diderm/T5aSS", "T5aSS"),
        ("anything", "TXSScan/bacteria/diderm/T6SSi", "T6SSi"),
        ("anything", "TXSScan/foo/bar/Flagellum", "Flagellum"),
        ("anything", "TXSScan/bacteria/diderm/T5aSS/", "T5aSS"),  # trailing slash tolerated
        # 2. Double-underscore fallback
        ("contigA__T1SS", "", "T1SS"),
        # 3. Regex match for SS-type substrings
        ("contig_X_T2SS_1", "", "T2SS"),
        ("scaffold-7_T6SSi_2", "", "T6SSi"),
        ("rep1_Flagellum_3", "", "Flagellum"),
        ("rep1_pT4SSt_1", "", "pT4SSt"),
        # 4. Identity fallback when no pattern matches
        ("totally_opaque_id", "", "totally_opaque_id"),
    ],
)
def test_parse_sys_id(sys_id, model_fqn, expected):
    assert parse_sys_id(sys_id, model_fqn) == expected


