# parse_macsyfinder_results — TSV reader for best_solution.tsv
# ---------------------------------------------------------------------------


_BEST_SOLUTION_HEADER = "sys_id\tmodel_fqn\thit_id\tgene_name\thit_status\tsys_wholeness"


def _write_best_solution(path, rows, *, header=True, prefix_comments=True):
    """Build a best_solution.tsv with realistic comment headers + data rows."""
    with open(path, "w") as f:
        if prefix_comments:
            f.write("# This file is generated by MacSyFinder\n")
            f.write("# version: 2.1.4\n")
            f.write("\n")
        if header:
            f.write(_BEST_SOLUTION_HEADER + "\n")
        for r in rows:
            f.write(
                "\t".join(
                    str(r.get(k, ""))
                    for k in [
                        "sys_id",
                        "model_fqn",
                        "hit_id",
                        "gene_name",
                        "hit_status",
                        "sys_wholeness",
                    ]
                )
                + "\n"
            )


def _component(sys_id, hit_id, gene_name="g", *, model_fqn="", wholeness=1.0, hit_status="mandatory"):
    return {
        "sys_id": sys_id,
        "model_fqn": model_fqn,
        "hit_id": hit_id,
        "gene_name": gene_name,
        "hit_status": hit_status,
        "sys_wholeness": wholeness,
    }


class TestParseMacsyfinderResults:
    def test_groups_components_by_sys_id(self, tmp_dir):
        path = os.path.join(tmp_dir, "best_solution.tsv")
        _write_best_solution(
            path,
            [
                _component("contig_T2SS_1", "GENE_A", "gspD"),
                _component("contig_T2SS_1", "GENE_B", "gspE"),
                _component("contig_T1SS_1", "GENE_C", "tolC"),
            ],
        )
        systems = parse_macsyfinder_results(tmp_dir)
        by_id = {s["sys_id"]: s for s in systems}
        assert len(by_id["contig_T2SS_1"]["components"]) == 2
        assert len(by_id["contig_T1SS_1"]["components"]) == 1

    def test_uses_model_fqn_when_present(self, tmp_dir):
        path = os.path.join(tmp_dir, "best_solution.tsv")
        _write_best_solution(
            path,
            [
                _component("opaque_id", "GENE_A", model_fqn="TXSScan/bacteria/diderm/T5aSS"),
            ],
        )
        systems = parse_macsyfinder_results(tmp_dir)
        assert systems[0]["ss_type"] == "T5aSS"

    def test_invalid_wholeness_falls_back_to_zero(self, tmp_dir):
        path = os.path.join(tmp_dir, "best_solution.tsv")
        _write_best_solution(
            path,
            [
                _component("contig_T2SS_1", "GENE_A", wholeness="not-a-number"),
            ],
        )
        systems = parse_macsyfinder_results(tmp_dir)
        assert systems[0]["wholeness"] == 0.0

    def test_skips_blank_and_comment_lines(self, tmp_dir):
        # The fixture writer already prepends comments + blank line; verify the
        # parser still picks up the data rows correctly.
        path = os.path.join(tmp_dir, "best_solution.tsv")
        _write_best_solution(path, [_component("contig_T2SS_1", "GENE_A")])
        systems = parse_macsyfinder_results(tmp_dir)
        assert len(systems) == 1

    # Note: a `if not sys_id: continue` guard exists in parse_macsyfinder_results
    # but is effectively unreachable. The parser does `line.strip()` on every
    # row first, which collapses leading empty TSV fields and shifts the
    # remaining columns left — so a row with an empty sys_id never surfaces
    # as `sys_id == ""` in the dict. Real MacSyFinder output never has an
    # empty sys_id, so this is a defensive-but-dead branch. Not testing it.

    def test_sample_prefixed_filename_resolved(self, tmp_dir):
        # Some MacSyFinder runs name the output {sample}_best_solution.tsv —
        # parser falls back to a directory glob if best_solution.tsv is absent.
        path = os.path.join(tmp_dir, "mysample_best_solution.tsv")
        _write_best_solution(path, [_component("contig_T2SS_1", "GENE_A")])
        systems = parse_macsyfinder_results(tmp_dir)
        assert len(systems) == 1

    def test_missing_file_returns_empty_list(self, tmp_dir):
        # No best_solution.tsv at all → empty list (logged warning, no crash)
        assert parse_macsyfinder_results(tmp_dir) == []

    def test_empty_file_returns_empty_list(self, tmp_dir):
        path = os.path.join(tmp_dir, "best_solution.tsv")
        _write_best_solution(path, [], header=False)
        assert parse_macsyfinder_results(tmp_dir) == []


