def _run_validate(
    monkeypatch,
    tmp_dir,
    msf_rows,
    *,
    wholeness_threshold=0.8,
    excluded_systems="Flagellum,Tad,T3SS",
):
    msf_dir = os.path.join(tmp_dir, "msf")
    os.makedirs(msf_dir, exist_ok=True)
    _write_best_solution(os.path.join(msf_dir, "best_solution.tsv"), msf_rows)

    gene_info = os.path.join(tmp_dir, "gene_info.tsv")
    with open(gene_info, "w") as f:
        f.write("locus_tag\tcontig\tstart\tend\n")  # required arg, content unused

    out_components = os.path.join(tmp_dir, "ss_components.tsv")
    out_systems = os.path.join(tmp_dir, "valid_systems.tsv")
    run_script_main(
        monkeypatch,
        validate_main,
        [
            "validate_macsyfinder_systems",
            "--msf-dir",
            msf_dir,
            "--gene-info",
            gene_info,
            "--sample",
            "test_sample",
            "--wholeness-threshold",
            str(wholeness_threshold),
            "--excluded-systems",
            excluded_systems,
            "--out-components",
            out_components,
            "--out-systems",
            out_systems,
        ],
    )
    return read_tsv_rows(out_components), read_tsv_rows(out_systems)


class TestMainEndToEnd:
    def test_wholeness_filter_drops_low_systems(self, monkeypatch, tmp_dir):
        rows = [
            _component("good_T2SS_1", "GENE_A", wholeness=0.95),
            _component("weak_T2SS_2", "GENE_B", wholeness=0.5),
        ]
        _, systems = _run_validate(monkeypatch, tmp_dir, rows)
        assert {s["sys_id"] for s in systems} == {"good_T2SS_1"}

    def test_excluded_systems_marked_but_kept(self, monkeypatch, tmp_dir):
        # Excluded systems still appear in BOTH outputs but with excluded=True
        rows = [
            _component("contig_T2SS_1", "GENE_A", model_fqn="TXSScan/bacteria/diderm/T2SS"),
            _component("contig_Flagellum_1", "GENE_F", model_fqn="TXSScan/bacteria/diderm/Flagellum"),
        ]
        components, systems = _run_validate(monkeypatch, tmp_dir, rows)
        excluded_flag = {s["sys_id"]: s["excluded"] for s in systems}
        assert excluded_flag["contig_T2SS_1"] == "False"
        assert excluded_flag["contig_Flagellum_1"] == "True"
        # Both still appear in component detail (audit trail)
        assert {c["sys_id"] for c in components} == {
            "contig_T2SS_1",
            "contig_Flagellum_1",
        }

    def test_n_components_correct(self, monkeypatch, tmp_dir):
        rows = [
            _component("contig_T2SS_1", "GENE_A", "gspD", model_fqn="TXSScan/bacteria/diderm/T2SS"),
            _component("contig_T2SS_1", "GENE_B", "gspE", model_fqn="TXSScan/bacteria/diderm/T2SS"),
            _component("contig_T2SS_1", "GENE_C", "gspF", model_fqn="TXSScan/bacteria/diderm/T2SS"),
        ]
        _, systems = _run_validate(monkeypatch, tmp_dir, rows)
        assert systems[0]["n_components"] == "3"

    def test_components_carry_sample_id(self, monkeypatch, tmp_dir):
        rows = [_component("contig_T2SS_1", "GENE_A", model_fqn="TXSScan/bacteria/diderm/T2SS")]
        components, _ = _run_validate(monkeypatch, tmp_dir, rows)
        assert all(c["sample_id"] == "test_sample" for c in components)

    def test_custom_excluded_systems(self, monkeypatch, tmp_dir):
        # User passes --excluded-systems="" → nothing excluded → flagellum kept
        rows = [_component("contig_Flagellum_1", "GENE_F", model_fqn="TXSScan/bacteria/diderm/Flagellum")]
        _, systems = _run_validate(
            monkeypatch,
            tmp_dir,
            rows,
            excluded_systems="",
        )
        assert systems[0]["excluded"] == "False"

    def test_no_systems_writes_header_only(self, monkeypatch, tmp_dir):
        components, systems = _run_validate(monkeypatch, tmp_dir, [])
        assert components == []
        assert systems == []
        # Header still present
        with open(os.path.join(tmp_dir, "valid_systems.tsv")) as f:
            assert f.readline().strip()
